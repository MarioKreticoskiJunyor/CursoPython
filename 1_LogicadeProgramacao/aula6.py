# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool

print(1+1)                      # le que sao dois numeros inteiros e com o sinal matematico"+" ele faz a soma
print('a'+'b')                  # ele identific que sao duas string e com o sinal matematico ele as juntam
print(int('1'), type(int('1'))) 
print(type(float('1') + 1))
print(bool(' '))
print(str(11) + 'b')
